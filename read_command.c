#include	"minishell.h"

void	read_command(char *cmd)
{
	if (cmd[0] == 'p' && cmd[1] == 'w' && cmd[2] == 'd')
		ft_pwd(cmd);
	else if (cmd[0] == 'e' && cmd[1] == 'c' && cmd[2] == 'h' && cmd[3] == 'o')
		ft_echo(cmd);
}